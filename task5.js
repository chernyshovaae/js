function f_plus(a, b) {
    return a + b;
}

function f_dif(a, b) {
    return a - b;
}


function f_mult(a, b) {
    return a * b;
}


function f_div(a, b) {
    return a / b;
}


var a = 2;
var b = 4;
console.log(f_plus(a, b));
console.log(f_dif(a, b));
console.log(f_mult(a, b));
console.log(f_div(a, b));